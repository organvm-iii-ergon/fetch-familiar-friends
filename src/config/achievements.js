// Get all achievement keys
export const ACHIEVEMENT_KEYS = Object.keys(ACHIEVEMENT_DEFINITIONS);

// Get achievements by category
export function getAchievementsByCategory(category) {
  return Object.values(ACHIEVEMENT_DEFINITIONS).filter(a => a.category === category);
}

// Get achievement by key
export function getAchievementDefinition(key) {
  return ACHIEVEMENT_DEFINITIONS[key] || null;
}

// Calculate XP with rarity multiplier
export function calculateAchievementXp(achievement) {
  return Math.round(achievement.xp * achievement.rarity.xpMultiplier);
}

// Get total possible achievements count
export function getTotalAchievementCount() {
  return ACHIEVEMENT_KEYS.length;
}

// Get visible achievements (non-hidden)
export function getVisibleAchievements() {
  return Object.values(ACHIEVEMENT_DEFINITIONS).filter(a => !a.hidden);
}

export default ACHIEVEMENT_DEFINITIONS;
